package lt.viko.eif.stamulyte.model;

public class Rusis {
    private int id;
    private String rusis;

    public Rusis(int id, String rusis) {
        this.id = id;
        this.rusis = rusis;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRusis() {
        return rusis;
    }

    public void setRusis(String rusis) {
        this.rusis = rusis;
    }
}

